<script>
    $(document).ready(function() {
        $('.rate-doctor-form').on('submit', function(e) {
            e.preventDefault();
            const form = $(this);
            const doctorId = form.data('doctor-id');
            const rating = form.find('input[name="rating"]:checked').val();
            const review = form.find('textarea[name="review"]').val();

            if (!rating) {
                alert('Please select a rating');
                return;
            }

            $.ajax({
                url: '../rate_doctor.php',
                method: 'POST',
                data: {
                    doctor_id: doctorId,
                    rating: rating,
                    review: review
                },
                success: function(response) {
                    const result = JSON.parse(response);
                    if (result.status === 'success') {
                        alert('Rating submitted successfully!');
                        location.reload();
                    } else {
                        alert(result.message || 'Error submitting rating');
                    }
                },
                error: function() {
                    alert('Error submitting rating');
                }
            });
        });
    });
</script>
